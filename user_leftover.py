#program to calculate user leftover for the month by accepting input from the user using the int data type
monthly_salary=input("pls input your monthly salary:")
monthly_expense=input("pls input your monthly expense:")
salary=int(monthly_salary)
expense=int(monthly_expense)

user_leftover=salary-expense
print(user_leftover)

